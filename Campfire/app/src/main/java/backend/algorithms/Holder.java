package algorithms;

/*
 * A holder class for double values in a Hashmap.
 * 
 */
public class Holder {

	private double value;

	public Holder(double value) {
		super();
		this.value = value;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
	
}
